    <p dir="RTL" style="text-align: justify;">
      <strong
        ><span style="font-family: georgia,serif;"
          >"<span dir="LTR">Animals</span>" از پینک فلوید یکی از آلبوم هایی است
          که در بین آلبوم های کلاسیک راک بسیار دست کم گرفته شده است. این آلبوم
          مانند یک فیلم سینمایی است که در آن تمامی بخش ها ، فصل ها و داستان های
          فرعی همگی با هم یکی می شوند تا به یک پایان مشخص برسند. هماهنگی و
          همرنگی بین آهنگ ها فوق العاده است.</span
        ></strong
      >
    </p>
    <!--more-->
    <p dir="RTL" style="text-align: justify;">
      <strong
        ><span style="font-family: georgia,serif;"
          >وقتی این مساله را در نظر بگیرید که دو قطعه از سه قطعه گسترش یافته و
          اصلی در این آلبوم در واقع از آهنگ هایی بوجود آمدند که از آلبوم قبلی
          گروه "<span dir="LTR"> Wish You Were Here</span>" حذف شده بودند ،
          مساله هماهنگی عجیب و فوق العاده بین آهنگ های آلبوم ، بازهم عجیب تر
          میشود.</span
        ></strong
      >
    </p>
    <p dir="RTL" style="text-align: justify;">
      <strong
        ><span style="font-family: georgia,serif;"
          >مفهموم و تصویر کلی آلبوم توسط بیسیت و آهنگ نویس اصلی گروه راجر واترز
          (<span dir="LTR">Roger Waters</span>) پایه ریزی شد. واترز حیوانات یک
          مزرعه را به جنبه های مختلف شخصیتی انسانها تشبیه کرد.</span
        ></strong
      >
    </p>
    <p dir="RTL" style="text-align: justify;">
      <strong
        ><span style="font-family: georgia,serif;"
          >در پس محتوای اشعار ، این آلبوم از لحاظ موسیقایی هم بسیار منحصر بفرد
          است. در بین همه ی آلبوم های پینک فلوید این آلبوم بیشترین نقش و نگار و
          محوریت هارد راک (<span dir="LTR">hard-rock</span>) را دارد. و البته
          آخرین آلبومی است که در آن قسمت های بی کلام گسترش یافته و آهنگ هایی با
          زمان بیش از 10 دقیقه وجود دارد. می توان گفت این آلبوم یک پل است بین
          آلبوم های سابق گروه که در آنها نقش همگی افراد پر رنگ بود و آلبوم های
          اواخر دهه هفتاد که واترز بر آنها مسلط شده بود (هر چه از اولین آلبوم به
          سمت آخرین آلبوم پینک فلوید برویم نقش واترز پررنگ تر و نقش بقیه اعضا
          کمتر می شود. در بین کارهای گروه ، میتوان از آلبوم "<span dir="LTR"
            ><a
              href="http://badgusan.com/pink-floyd-the-dark-side-of-the-moon-1973/"
              >The dark side of the moon</a
            ></span
          >" به عنوان بهترین عملکرد تیمی نام برد). در آلبوم های قبلی با اینکه
          واترز بخش های بزرگی از موسیقی آلبوم را می نوشت ، دیوید گیلمور (<span
            dir="LTR"
            >David Gilmour</span
          >) نقش خواننده ی اصلی را بر عهده داشت (البته بعد از جدا شدن
          <span dir="LTR">Syd Barrett</span>). در آلبوم "<span dir="LTR">
            Animals</span
          >" ، همه چیز عوض شد و گیلمور تنها در بخش های کوچکی خواند در حالی که
          واترز در همه جاهای دیگر می خواند.</span
        ></strong
      >
    </p>
    <p dir="RTL" style="text-align: justify;">
      <strong
        ><span style="font-family: georgia,serif;"
          >موضوع آلبوم یک واکنش است به وضعیت موسیقی راک در زمانی که یک سبک جدید
          به نام پانک (<span dir="LTR">punk</span>) شروع به منفجر شدن در شهر
          لندن کرده بود. از جمله الزامات این جنبش جدید و ساده (پانک) این بود که
          بر علیه هنرمندان قدیمی تر متحد شوند و فعالیت کنند و پینک فلوید تبدیل
          به یک هدف تکراری و مناسب برای این خشم شده بود. با این وجود ، از وارد
          شدن بعضی از اعضای گروه به این جنبش استقبال شد و دلیل آن هم ادای دینی
          به گذشته و اصالت زیرزمینی گروه بود.</span
        ></strong
      >
    </p>
    <p dir="RTL" style="text-align: justify;">
      <strong
        ><span style="font-family: georgia,serif;"
          >آلبوم "<span dir="LTR"> Animals</span>" برای اکثر اعضای گروه بسیار
          استرس زا بود ، چرا که بر روی افراد مشخص و یا طرز فکر آنها و آن هم از
          دیدگاه واترز ، تمرکز داشت. واترز هم با خوشحالی سلطنت را در دست گرفته
          بود و آلبوم را با تصویر ذهنی خودش تولید می کرد. با این وجود ، به جز
          آلبوم "<a
            href="http://badgusan.com/pink-floyd-the-dark-side-of-the-moon-1973/"
            >The dark side of the moon</a
          >" این آلبوم از لحاظ صوتی و موسیقایی بیشترین جوایز و تمجید ها را ازآن
          خود کرده و به طور مستمر در جدول هواداران مشتاق پینک فلوید بالا می رود.
          باید گفت این آلبوم برای شنوندگان غیر جدی مناسب نیست. برای عاشقان
          موسیقی است ، کسانی که غیرعادی بودن جذابش را لمس می کنند. همین عجیب و
          غریب و غیر عادی بودن این آلبوم باعث شده است که در طول گذشت چند دهه از
          ساخت آن همچنان صدا و نوایی تازه داشته باشد.</span
        ></strong
      >
    </p>
    <p dir="RTL" style="text-align: justify;">
      <strong
        ><span style="font-family: georgia,serif;"
          >در سال 1974 ، پینک فلوید تور "<span dir="LTR">
            Wish You Were Here</span
          >
          " را به راه انداختند و قطعات جدیدی را از آلبومی به همین اسم ، پیشاپیش
          در کنسرت ها اجرا می کردند. وقتی کار به ضبط آلبوم "<span dir="LTR">
            Wish You Were Here</span
          >" رسید ، دو تا از این قطعات به طور کامل از آلبوم کنار گذاشته شدند و
          بعدا برای آلبوم "<span dir="LTR"> Animals</span>" باز نویسی شدند. یکی
          از این آهنگ ها قطعه ی آکوستیک جز به نام "<span dir="LTR">
            You Gotta Be Crazy</span
          >" از گیلمور بود که آرام تر شد و اشعارش مجددا نوشته شد و به "<span
            dir="LTR"
          >
            Dogs</span
          >" تغییر نام داد.</span
        ></strong
      >
    </p>
    <p dir="RTL" style="text-align: justify;">
      <strong
        ><span style="font-family: georgia,serif;"
          >"<span dir="LTR"> Dogs</span>" آهنگ بسیار منحصربفرد ومتفاوتی است حتی
          در بین کاتالوگ وسیع پینک فلوید. با یک توالی عجیب از آکوردها از گیلمور
          و نوازندگی بجا و دقیق <span dir="LTR">organ</span> و
          <span dir="LTR">synth</span> از کیبوردیست گروه ریچارد رایت (<span
            dir="LTR"
            >Richard Wright</span
          >) ، یک موزیک لایه بندی شده بوجود آمد که هر چه در سه
          <span dir="LTR">verse</span> اول پیش می رفت ، شدت می گرفت. وقتی که
          اولین تکنوازی گیتار وارد می شود ، کاملا مشخص است که گیلمور با لهجه دست
          آشنای خودش در حال امضا کردن آلبوم است. و با پیشروی آرام در میان اولین
          قسمت بی کلام ، یکی از بهترین آثار پینک فلوید بوجود می آید. اشعار گزنده
          و بدبینانه ، فلسفه هایی از ماکیاولی (<span dir="LTR">Machiavelli</span
          >) ، سائول الینسکی (<span dir="LTR">Sol Alinsky</span>) و ویتو
          کورلئونه (<span dir="LTR">Vito Corleone</span>) را با هم ترکیب می کند
          و هیچ تعادلی در تعقیب و بدست آوردن قدرت را پیشنهاد نمی دهد. در میان
          زمان هشت و نیم دقیقه ای این آهنگ ، یک قسمت طولانی از نوازندگی
          <span dir="LTR">synth</span> و پارس سگ وجود دارد که درنگاه اول به نظر
          فقط قسمتی است که آهنگ را پر کرده و ارزش دیگری ندارد. اما وقتی بیشتر به
          آن توجه کنید متوجه می شوید که چقدر هنرمندانه آهنگ را به طور کامل به
          طرف دیگر (آهنگ های بعدی) می برد. حالا واترز می خواند ، حتی گیتار
          آکوستیک دقیقا همان آکوردها را می نوازد اما موسیقی کاملا متفاوتی به گوش
          می رسد.</span
        ></strong
      >
    </p>
    <p dir="RTL" style="text-align: justify;">
      <strong
        ><span style="font-family: georgia,serif;"
          >"<span dir="LTR">Dogs</span>" همچنین یک لینک است به آهنگ آینده گروه
          "<span dir="LTR">Hey you</span>" از آلبوم "<span dir="LTR"
            >The Wall</span
          >" ، جایی که در آن مفهموم <span dir="LTR"> “</span
          ><span dir="LTR">bad blood</span> <span dir="LTR">stone </span>" به
          طور کامل بازگو می شود. در انتها در قسمت "<span dir="LTR"
            >who was …</span
          >" ("چه کسی بود ...)" که نتیجه گیری این آهنگ 17 دقیقه ای است ، واترز
          شعری به نام "<span dir="LTR"> Howl</span>" از شاعر معروف الن گینزبرگ
          (<span dir="LTR">Alan Ginsberg</span>) را قرض می گیرد.</span
        ></strong
      >
    </p>
    <p dir="RTL" style="text-align: justify;">
      <strong
        ><span style="font-family: georgia,serif;"
          >"<span dir="LTR"> Sheep</span>" آهنگ دیگری است از همان تور سال 1974 ،
          که در آن زمان به طور کلی یک قطعه بی کلام بود و "<span dir="LTR">
            Raving and Drooling</span
          >
          " نام داشت. این آهنگ از یک صدای سنگین
          <span dir="LTR">synth</span> تشکیل شده و در آن ، روی صدای واترز یک
          افکت افسار گسیخته قرار گرفته که تا انتهای خطوط
          <span dir="LTR">verse</span> ادامه دارد. اشعار تند و سرزنش کننده
          هستند.</span
        ></strong
      >
    </p>
    <p dir="RTL" style="text-align: justify;">
      <strong
        ><span style="font-family: georgia,serif;"
          >این آهنگ بیش از 10 دقیقه ای شامل یک قسمت میانی می شود که توسط همان
          مفهوم <span dir="LTR">“stone”</span> به آهنگ
          <span dir="LTR">“Dogs”</span> باز می گردد و بعد از آن وارد قسمتی می
          شویم که نیک میسون (<span dir="LTR">Nick Mason</span>) درامر گروه ،
          جملاتی را از طریق <span dir="LTR">Vocoder</span> (نوعی سینتیسایزر)
          بیان میکند. در انتها هم به یک <span dir="LTR">verse</span> می رسیم که
          در آن گوسفند ها راه را برای انتقام گرفتن از سگ ها هموار می کنند.</span
        ></strong
      >
    </p>
    <p dir="RTL" style="text-align: justify;">
      <strong
        ><span style="font-family: georgia,serif;"
          ><span dir="LTR">“Pigs On the Wing”</span> یک قطعه آکوستیک فولک (<span
            dir="LTR"
            >folk</span
          >) خالص است. مانند نواهای آرام شده باب دیلن (<span dir="LTR"
            >Bob</span
          >
          <span dir="LTR">Dylan</span>) اما با خوانندگی متفاوت از واترز. این
          آهنگ به صورت یک تک آهنگ و با تکنوازی گیتار بین
          <span dir="LTR">verse</span> هایش از گیتاریست دوم گروه که فقط در تور
          ها شرکت داشت (<span dir="LTR">Snowy White</span>) ضبط شده بود. اما طی
          یک تصمیم (که بعدها معلوم شد بسیار زیرکانه بوده) ، واترز این آهنگ را به
          دو قسمت تقریبا مساوی تقسیم و تکنوازی گیتار را حذف کرد. و همچنین بطور
          قابل ملاحظه ای حق امتیاز خودش را در آلبوم افزایش داد. این حرکت به شکل
          بسیار شدیدی از طرف گیلمور مورد اعتراض قرار گرفت.</span
        ></strong
      >
    </p>
    <p dir="RTL" style="text-align: justify;">
      <strong
        ><span style="font-family: georgia,serif;"
          >آهنگ <span dir="LTR">“Pigs (Three Different Ones)”</span> (که نباید
          آن را با <span dir="LTR">“Pigs on the Wing”</span> اشتباه گرفت) سومین
          قطعه بزرگ در این آلبوم است. از لحاظ موسیقایی این آهنگ بسیار می درخشد ،
          بخصوص زمانی که کیبورد رایت در کنار گیتار تند و تیز و نیش دار گیلمور که
          یک افکت جالب دارد قرار می گیرد. از لحاظ شعری ، واترز با هدف و منظور با
          صاحبان ثروت و قدرت صحبت می کند ، به طوری که می توان گفت یک نسخه بروز
          شده از <span dir="LTR">“Us and Them”</span> (از آلبوم
          <span dir="LTR"
            ><a
              href="http://badgusan.com/pink-floyd-the-dark-side-of-the-moon-1973/"
              >The dark side of the moon</a
            ></span
          >) است که کاملا توجه به آنها (<span dir="LTR">“Them”</span>) دارد
          (آهنگ<span dir="LTR">“Us and Them”</span> به معنی ما و آهنهاست در حالی
          که آهنگ <span dir="LTR">“Pigs(Three Different Ones)”</span> فقط با
          دیگران سخن می گوید و از آنها می گوید).</span
        ></strong
      >
    </p>
    <p dir="RTL" style="text-align: justify;">
      <strong
        ><span style="font-family: georgia,serif;"
          >پیرو انتشار آلبوم "<span dir="LTR">Animals</span>" ، گروه شروع به
          برگزاری بزرگترین تور تا آن زمان کرد. توری به نام "<span dir="LTR"
            >In the Flesh</span
          >". این تور اولین تجربه پینک فلوید در اجراهای بزرگ استادیومی بود. و
          اعضا خود را در شرایط ناراحتی پیدا کردند و این مساله باعث شد جر و بحث
          های بسیار بیشتری در بگیرد. این تور همچنین صحنه و فضایی برای آلبوم بعدی
          گروه به نام "<span dir="LTR"> The Wall</span>" بود. این آلبوم بسیار
          محبوب شد و تعداد فوق العاده زیادی شنونده پیدا کرد. چیزی که آلبوم
          "<span dir="LTR">Animals </span>" نتوانست بدست بیاورد. با این حال
          "<span dir="LTR">Animals </span>" آلبوم بسیار بزرگیست و دربین هم دوره
          های راک اند رولی خود بسیار منحصر بفرد و قابل تشخیص است.</span
        ></strong
      >
    </p>

