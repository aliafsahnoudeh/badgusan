          انتشار دهد. خیلی ها این آهنگ را به گونه ای نسخه خلاصه شده ی آلبوم
          <span dir="LTR">“The Wall”</span> می دانند. آهنگ با یک صدای "پینگ"
          شبیه آنچه در زیر دریایی می شنویم شروع می شود. این صدا به صورت تجربی
          توسط ریچارد رایت (<span dir="LTR">Richard Wright</span>) کیبوردیست
          گروه ساخته شده است. فضا آهسته آهسته شکل می گیرد و صداهای دیگری از جمله
          یک آرپژ از کیبورد اضافه می شوند تا گیتار آرام و ملایم گیلمور وارد می
          شود. همنوازی گیلمور و رایت به آرامی ادامه پیدا می کند و از فضایی به
          فضای دیگر می رود تا وارد <span dir="LTR">verse </span> ها و
          <span dir="LTR">chorus</span> های آهنگ می شویم که شامل یک همخوانی
          بسیار زیبا از گیلمور و رایت و یک <span dir="LTR">Riff</span> به یاد
          ماندنی کروماتیک گیتار است. آهنگ چند بار بین این دو قسمت جا به جا می
          شود و با تک نوازی گیلمور ، این <span dir="LTR">Riff</span> ها رنگ و
          بوی دیگری می گیرند و دائم در حال به اوج رفتن هستند که در نهایت به یک
          باره متوقف می شوند. صدای بیس واضح تر شده ی واترز ، کیبورد رایت را
          همراهی می کند تا از یک طرف گیلمور با یک افکت زیبا و منحصر بفرد و از
          طرف دیگر رایت با کیبورد خود به نوبت تکنوازی کنند. این بخش نسبتا طولانی
          آنقدر زیبا و حساب شده است و ریز کاری دارد که متوجه گذر زمان نمی شوید.
          صدا ها رفته رفته محو می شوند تا به بخش طولانی میانی می رسیم. یک فضای
          مهیب و ترسناک که با صدای گیتار گیلمور بسیار وهم برانگیز تر هم می شود.
          در پس زمینه صدای کلاغ ها می شنویم و انگار گیتار حرف می زند یا ناله می
          کند. تازه در اینجاست که متوجه می شویم که نام آهنگ چرا
          <span dir="LTR">“Echoes”</span> یعنی "پژواک های صدا یا انعکاس های صدا"
          است. همینطور که در گیر این صدا بازی ها هستیم ، یک صدای فوق العاده روشن
          ، به آرامی به گوش می رسد. این صدا رفته رفته زیاد می شود ، مانند نوری
          که از دل تاریکی (آن فضای وهم آلود و ترسناک) به بیرون می تابد. با زیاد
          تر شدن این صدا ، فضای روشن نیز رفته رفته آشکار تر می گردد. انگار کور
          سوی امید دارد قوی تر می شود. باید گفت همین بخش میانی آهنگ ، یعنی قسمت
          تاریک و تبدیل شدنش به یک حس امیدواری یکی از بی نظیر ترین صداهای ساخته
          شده توسط پینک فلوید را به دوستداران موسیقی هدیه می دهد. صدای "پینگ"
          کیبورد رایت هم دوباره به گوش می رسد. در پشت صدای اصلی یک سری صداهای
          دیگر دارند شکل می گیرند و پرنگ تر می شوند. با واضح تر شدن آنها
          آکوردهای صدای روشن اصلی کیبورد هم تغییر می کند. گیتار بیس وارد می شود.
          سپس ریتم متوالی گیتار گیلمور می آید و در نهایت درامز اضافه می شود. این
          <span dir="LTR">Riff</span> ها چند بار تکرار می شود و یک تک نوازی آرام
          کیبورد بر فراز آن جریان دارد. و ناگهان همگی تمام می شوند تا آرپژ
          شاهکار گیلمور به گوش برسد که در پس زمینه با گیتار اسلایدری از خودش
          همراه است. دوباره به <span dir="LTR">Riff</span> های بعدی بر میگردیم و
          در نهایت همان <span dir="LTR">verse</span> ها و
          <span dir="LTR"> chorus</span> های ابتدای آهنگ را می شنویم اما این بار
          با شدت و ضرب بیشتر. در نهایت همان قسمت اول یعنی کیبورد رایت و تکنوازی
          گیلمور و این بار با همراهی واترز و میسون تکرار می شود و همگی محو می
          شوند تا فقط یک سری اکوی در هم ریخته به گوش برسد.</span
        ></strong
      >
    </p>
